package com.example.tourism.entity;

public class FabulousDetails {
    private Integer fabulousDetailsId;

    private Integer userId;

    private Integer exhibitsId;

    private Integer fabulousNumber;

    private Integer flag;

    public Integer getFabulousDetailsId() {
        return fabulousDetailsId;
    }

    public void setFabulousDetailsId(Integer fabulousDetailsId) {
        this.fabulousDetailsId = fabulousDetailsId;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getExhibitsId() {
        return exhibitsId;
    }

    public void setExhibitsId(Integer exhibitsId) {
        this.exhibitsId = exhibitsId;
    }

    public Integer getFabulousNumber() {
        return fabulousNumber;
    }

    public void setFabulousNumber(Integer fabulousNumber) {
        this.fabulousNumber = fabulousNumber;
    }

    public Integer getFlag() {
        return flag;
    }

    public void setFlag(Integer flag) {
        this.flag = flag;
    }
}